    private void regButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regButtonActionPerformed
        //Submit Button Code

        if (!regNameTextField.getText().equals("") && !regLastNameTextField.getText().equals("") && !regUserIdTextField.getText().equals("")
                && !regPasswordField.getPassword().equals("") && !regPasswordFieldConfirm.getPassword().equals("")) {

            String userName = regNameTextField.getText();
            String userLName = regLastNameTextField.getText();
            long userId = Long.parseLong(regUserIdTextField.getText());
            char[] password = regPasswordField.getPassword();
            char[] passwordConfirm = regPasswordFieldConfirm.getPassword();
            String userPosition = regPositionComboBox.getSelectedItem().toString();
            
           
            
            boolean confirmedUN = validateUserId(userId);
            if (!confirmedUN) {
                JOptionPane.showMessageDialog(this,
                        "Somebody already has this userID.",
                        "Invalid UserID",
                        JOptionPane.ERROR_MESSAGE);
            }
            boolean confirmedPW = validatePassword(password, passwordConfirm);
            if (confirmedPW && confirmedUN) {
                //SQL Insert
                DBManager dbm = new DBManager(userName, userLName, userId, password, userPosition);
                boolean success = dbm.register();
                if (success) {
                    System.out.println("Registration Successful");
                    //Register Button Code
                    this.setVisible(false);
//                //Register jFrame
//                FormLogin formLogin = new FormLogin();
//                formLogin.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Registration Failed.",
                            "Registration Failed",
                            JOptionPane.ERROR_MESSAGE);
                    System.out.println("Registration Failed");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Fill all fields",
                    "Registration Failed",
                    JOptionPane.ERROR_MESSAGE);
            System.out.println("Registration Failed");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_regButtonActionPerformed

