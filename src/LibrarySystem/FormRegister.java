    private Boolean validateUserId(long userId) {
        DBManager dbm = new DBManager(userId);
        if (!dbm.validateUserId()) { //False - Username Exists
            return false;
        }
        return true;
    }

    
    
    private Boolean validatePassword(char[] password, char[] passwordConfirm) {
        if (!Arrays.equals(password, passwordConfirm)) {
            JOptionPane.showMessageDialog(this,
                    "Passwords don't match.",
                    "Invalid Password",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if ((password.length < 6) || (password.length == 0)) {
            JOptionPane.showMessageDialog(this,
                    "Password is less than 6 characters or blank.",
                    "Invalid Password",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

